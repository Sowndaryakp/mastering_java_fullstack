import React, { useState } from 'react';
import { useDispatch } from 'react-redux';
import { useNavigate } from 'react-router-dom';
import { loginStart, loginSuccess, loginFailure, DEMO_CREDENTIALS } from '../../store/slices/authSlice';

const Login = () => {
  const dispatch = useDispatch();
  const navigate = useNavigate();
  const [formData, setFormData] = useState({
    email: '',
    password: '',
  });
  const [error, setError] = useState('');

  const handleChange = (e) => {
    setFormData({
      ...formData,
      [e.target.name]: e.target.value,
    });
    setError('');
  };

  const handleSubmit = (e) => {
    e.preventDefault();
    dispatch(loginStart());
    
    // Check credentials against hardcoded values
    const user = Object.values(DEMO_CREDENTIALS).find(
      (cred) => cred.email === formData.email && cred.password === formData.password
    );

    if (user) {
      dispatch(loginSuccess(user));
      navigate('/'); // Navigate to dashboard after successful login
    } else {
      dispatch(loginFailure('Invalid credentials'));
      setError('Invalid email or password');
    }
  };

  const handleDemoLogin = (credentials) => {
    dispatch(loginStart());
    dispatch(loginSuccess(credentials));
    navigate('/');
  };

  return (
    <div className="min-h-screen flex items-center justify-center bg-gray-50 py-12 px-4 sm:px-6 lg:px-8">
      <div className="max-w-md w-full space-y-8">
        <div>
          <h2 className="mt-6 text-center text-3xl font-extrabold text-gray-900">
            Sign in to OEP
          </h2>
          {/* Demo credentials info */}
          <div className="mt-4 p-4 bg-blue-50 rounded-md">
            <h3 className="text-sm font-medium text-blue-800 mb-2">Demo Credentials:</h3>
            <div className="text-xs text-blue-700 space-y-1">
              <button
                onClick={() => handleDemoLogin(DEMO_CREDENTIALS.ADMIN)}
                className="w-full text-left hover:text-blue-900"
              >
                Admin: admin@oep.com / admin123
              </button>
              <button
                onClick={() => handleDemoLogin(DEMO_CREDENTIALS.TEACHER)}
                className="w-full text-left hover:text-blue-900"
              >
                Teacher: teacher@oep.com / teacher123
              </button>
              <button
                onClick={() => handleDemoLogin(DEMO_CREDENTIALS.STUDENT)}
                className="w-full text-left hover:text-blue-900"
              >
                Student: student@oep.com / student123
              </button>
            </div>
          </div>
        </div>
        <form className="mt-8 space-y-6" onSubmit={handleSubmit}>
          <div className="rounded-md shadow-sm -space-y-px">
            <div>
              <label htmlFor="email" className="sr-only">
                Email address
              </label>
              <input
                id="email"
                name="email"
                type="email"
                required
                className="appearance-none rounded-none relative block w-full px-3 py-2 border border-gray-300 placeholder-gray-500 text-gray-900 rounded-t-md focus:outline-none focus:ring-indigo-500 focus:border-indigo-500 focus:z-10 sm:text-sm"
                placeholder="Email address"
                value={formData.email}
                onChange={handleChange}
              />
            </div>
            <div>
              <label htmlFor="password" className="sr-only">
                Password
              </label>
              <input
                id="password"
                name="password"
                type="password"
                required
                className="appearance-none rounded-none relative block w-full px-3 py-2 border border-gray-300 placeholder-gray-500 text-gray-900 rounded-b-md focus:outline-none focus:ring-indigo-500 focus:border-indigo-500 focus:z-10 sm:text-sm"
                placeholder="Password"
                value={formData.password}
                onChange={handleChange}
              />
            </div>
          </div>

          {error && (
            <div className="text-red-600 text-sm text-center">
              {error}
            </div>
          )}

          <div>
            <button
              type="submit"
              className="group relative w-full flex justify-center py-2 px-4 border border-transparent text-sm font-medium rounded-md text-white bg-indigo-600 hover:bg-indigo-700 focus:outline-none focus:ring-2 focus:ring-offset-2 focus:ring-indigo-500"
            >
              Sign in
            </button>
          </div>
        </form>
      </div>
    </div>
  );
};

export default Login; 